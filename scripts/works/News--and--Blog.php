<div>
<h3>Programming a website... yowza!</h3></div>
<div>So I had no idea that there are so many different tools and technologies for programming a web page!&nbsp; I've finally completed the reservation system, for which I've had to learn PHP, SQL, HTML DOM, CSS, JQuery, AJAX methods, and PayPal's IPN API.&nbsp; It's taken me a few weeks, but it's finally wrapping up!</div>
<div>&nbsp;</div>
<div>The real kicker of it all is that I discovered, in my perusing of the PayPal SDK, that they also support inventory management, so I could've done the whole reservation system through them from the beginning and saved myself a bunch of time.&nbsp; Oh well, at least this way I've learned a bunch of useful web tools that I can use in the future! :)</div>
