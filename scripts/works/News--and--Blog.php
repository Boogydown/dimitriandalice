<h3>Cold!</h3></div>
<div>We just got back from a very cold day out at Moon River!  We went to go scope the place out, again, and take inventory of a few things.</div>
<div>&nbsp;</div>
<div>We keep falling for the place more and more every time we go out, so that's a good sign!  Means we done well in picking a venue!  We were led around by Mike, the Ranch Manager.  Extremely nice guy, and impressively enough had a huge part in the design and building of most of the lodges and buildings on the property.  Very cool!</div>
<div>&nbsp;</div>
<div>I took a few more pictures of the venue and of the rooms.  You can find them on the Venue and Accommodations pages, respectively.</div>  Go check 'em out! :)  The place does get greener, btw. :)
