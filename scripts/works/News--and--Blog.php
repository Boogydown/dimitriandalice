<hr style="width: 100%; height: 1px; background-color: #9f20df" align="center" color="#9f20df" noshade="noshade" /><em>October 1st<span>, 2010</span></em></div>
<div>
<h3>Venue finally decided on!</h3></div>
<div>UGG... searching for venues has got to be one of the most difficult
 parts of planning a wedding.&nbsp; It's not necessarily the incessant
searching and visiting, and so forth, to find the right place, it's the
uncertainty in the meantime that's so tough.&nbsp; Not just the emotional
aspect of it, but also the fact that it puts the rest of the planning on
 hold!&nbsp; Do we need table settings?&nbsp; How about accommodations?&nbsp; Travel
arrangements?&nbsp; Food?&nbsp; Ceremony and reception logistics?&nbsp; Ack!</div>
<div>&nbsp;</div>
<div>&nbsp;One of the difficulties was deciding on, exactly, the kind of
wedding that we wanted.&nbsp; Did we want a more period wedding, with a more
traditional structure?&nbsp; Did we want a more destination-wedding for us
and our attendees?&nbsp; Did we want activities or just the ceremony/reception?&nbsp; And on,
and on... We did know, at least, that we wanted it out in the woods, and
 that we wanted a party atmosphere.</div>
<div>&nbsp;</div>
<div>So now, at last, we have settled on Moon River Ranch, near Waco, TX.&nbsp; It is 1800 acres of awesome along the Brazos River.&nbsp; It's expensive, but it's gorgeous and will be a lot of fun!&nbsp; </div>
<div>&nbsp;</div>
<div>Link is <a title="" href="http://moonriverranch.com">here</a>.&nbsp; More info to come!</div>
