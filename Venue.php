<h2 align="center">Moon River Ranch</h2></div>
<div align="center"><em>&quot;Where time is measured in sunrises and sunsets.&quot;</em></div>
<div>&nbsp;</div>
<div>Located just east of Waco, TX (75 miles northeast of Austin), this ranch covers 1800 acres and 6 miles of the Brazos River.&nbsp; The look is rustic, the feel is cozy, the place is beautiful.&nbsp; </div>
<div>&nbsp;</div>
<div><img alt="" title="" src="documents/venue/photos_corporate_retreats_pleasant_day.jpg" align="left" width="350" height="250" /></div>The ranch is a gorgeous collection of luxurious accommodations,&nbsp; farmland, pecan orchards, fishing spots, and more.&nbsp; The breathtaking views, cascading waterfalls, shimmering pools, swaying palms and tranquil Brazos River all provide the perfect ambiance for a special wedding!&nbsp;
<div>&nbsp;</div>
<div>
<div>Although lounging and relaxing are perfect pastimes out there, there are numerous other fun activities.&nbsp; One option is to take a hay ride that winds through the ranch, providing the opportunity to see some of the many types of wildlife including bald eagles, fox, coyotes and great blue herons. you could stroll through the barnyard along with the chickens, ducks, guineas and pigs!&nbsp; Stargaze the night sky or just relax around a roaring fire in the fire pit.&nbsp; There is also horseback ridding, canoeing, swimming and hiking.&nbsp; The picturesque banks of the Brazos River also provide a natural white sand beach to sink you toes in and watch the sunset.</div></div>
